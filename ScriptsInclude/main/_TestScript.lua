function WriteToFile(output,file)
	local filehandle = openfile(file, 'a')
	if filehandle ~= nil then
		write(filehandle,"\n" .. output)
	else
		local filehandle = writeto(file)
		write(filehandle,"\n" .. output)  
	end
	flush(filehandle)
	closefile(filehandle)
end



